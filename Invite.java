public class Invite
{
    String name, user, age;

    public void setName(String name)
    {
        this.name = name;
    }
    public void setUser(String user)
    {
        this.user = user;
    }

    public void setAge(String age){
        this.age = age;
    }

    public String getName()
    {
        return this.name;
    }

    public String getUser()
    {
        return this.user;
    }

    public String getAge()
    {
        return this.age;
    }
}